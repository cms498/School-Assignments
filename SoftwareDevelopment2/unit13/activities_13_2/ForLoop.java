package activities_13_2;

public class ForLoop {
    public static void main(String[] args) {
        for(int i = 0; i <= 10; i++){
            System.out.println(i);
        }
        for(int x = 50; x <= 100; x++){
            System.out.println(x);
        }
        for(int q = 100; q >= 0; q-=2){
            System.out.println(q);
        }
    }
}
