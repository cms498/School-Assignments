public class Strings {
    public static void main(String[] args){
        String word = "butter";
        int length = word.length();
        System.out.println("First = " + word.charAt(0));
        System.out.println("Last = " + word.charAt(length - 1));
        System.out.println("Index 1 = " + word.charAt(1));
        System.out.println("Index 3 = " + word.charAt(3));
        System.out.println("Index 4 = " + word.charAt(4));
    }
}
