public class Primitives {
    public static void main(String[] args) {
        int age = 18;
        float pi = 3.14f;
        char b = 'b';
        boolean bool = false;

        System.out.println("Age = " + age);
        System.out.println("PI = " + pi);
        System.out.println("Char = " + b);
        System.out.println("Boolean = " + bool);
    }
}
